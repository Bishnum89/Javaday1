package One;

public class Revision {

	public static void main(String[] args) {

		// for loop
		for (int i = 1; i <= 5; i++) {
			System.out.println(i);
		}
		for (int i = 10; i >= 1; i--) {
			System.out.println(i);
		}

		for (int i = 2; i <= 20; i = i + 2) {
			System.out.println(i);
		}

		// for loop with break statement
		for (int i = 5; i >= 1; i--) {
			System.out.println(i);
			if (i == 2)
			{
				break;

			}
		}

		
		}	
		

	}


